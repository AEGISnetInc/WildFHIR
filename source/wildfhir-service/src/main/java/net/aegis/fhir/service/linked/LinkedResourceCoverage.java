package net.aegis.fhir.service.linked;

import java.util.ArrayList;
import java.util.List;
import java.util.logging.Logger;

import org.hl7.fhir.r4.model.Coverage;
import org.hl7.fhir.r4.model.Reference;
import org.hl7.fhir.r4.model.Resource;

import net.aegis.fhir.service.ResourceService;

/**
 * @author richard.ettema
 *
 */
public class LinkedResourceCoverage extends LinkedResourceProxy {

	private Logger log = Logger.getLogger("LinkedResourceCoverage");

	/* (non-Javadoc)
	 * @see net.aegis.fhir.service.linked.LinkedResourceProxy#getLinkedResources(net.aegis.fhir.service.ResourceService, org.hl7.fhir.instance.model.Resource)
	 */
	@Override
	public List<Resource> getLinkedResources(ResourceService resourceService, Resource containerResource) throws Exception {

		log.fine("[START] LinkedResourceCoverage.getLinkedResources()");

		List<Resource> linkedResources = new ArrayList<Resource>();

		try {
			// Test for valid container Resource
			if (containerResource != null && containerResource instanceof Coverage) {

				Coverage typedContainerResource = (Coverage) containerResource;

				/*
				 * Coverage linked Resource references
				 */
				String ref = null;
				Resource linkedResource = null;

				// policyHolder
				if (typedContainerResource.hasPolicyHolder() & typedContainerResource.getPolicyHolder().hasReference()) {

					ref = typedContainerResource.getPolicyHolder().getReference();
					linkedResource = this.getLinkedResourceAny(resourceService, ref);

					if (linkedResource != null) {
						linkedResources.add(linkedResource);
					}
				}

				linkedResource = null;
				// subscriber
				if (typedContainerResource.hasSubscriber() & typedContainerResource.getSubscriber().hasReference()) {

					ref = typedContainerResource.getSubscriber().getReference();
					linkedResource = this.getLinkedResourceAny(resourceService, ref);

					if (linkedResource != null) {
						linkedResources.add(linkedResource);
					}
				}

				linkedResource = null;
				// beneficiary
				if (typedContainerResource.hasBeneficiary() & typedContainerResource.getBeneficiary().hasReference()) {

					ref = typedContainerResource.getBeneficiary().getReference();
					linkedResource = this.getLinkedResource(resourceService, ref, "Patient");

					if (linkedResource != null) {
						linkedResources.add(linkedResource);
					}
				}

				linkedResource = null;
				// payor
				if (typedContainerResource.hasPayor()) {

					for (Reference payor : typedContainerResource.getPayor()) {
						if (payor.hasReference()) {

							ref = payor.getReference();
							linkedResource = this.getLinkedResourceAny(resourceService, ref);

							if (linkedResource != null) {
								linkedResources.add(linkedResource);
							}
						}
					}
				}

				linkedResource = null;
				// contract (Contract)
				if (typedContainerResource.hasContract()) {

					for (Reference contract : typedContainerResource.getContract()) {
						if (contract.hasReference()) {

							ref = contract.getReference();
							linkedResource = this.getLinkedResource(resourceService, ref, "Contract");

							if (linkedResource != null) {
								linkedResources.add(linkedResource);
							}
						}
					}
				}
			}

		} catch (Exception e) {
			// Exception caught
			e.printStackTrace();
			throw e;
        }

		return linkedResources;
	}

}
